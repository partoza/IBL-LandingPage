                            ${isMobile ? 'w-8 h-8' : 'w-10 h-10'} 
                            rounded-full flex items-center justify-center 
                            font-bold shadow-lg">
                        <svg class="${isMobile ? 'w-4 h-4' : 'w-5 h-5'}" 
